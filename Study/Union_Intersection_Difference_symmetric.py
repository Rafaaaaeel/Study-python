friends = {'Rafael','Sasha','Noah'}

another_friends = {'Rafael','Sasha'}

print(friends ^ another_friends) # Symmetric ## Return a new set the value duplicate

print(friends | another_friends) # Union ## Create a new set removing the duplicate value 

print(friends & another_friends) # Intersection ## Create a new set with the duplicate value

print(friends - another_friends) # Difference ## Return the duplicate Value